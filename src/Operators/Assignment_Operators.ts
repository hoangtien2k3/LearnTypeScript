//5. Assignment Operators (Toán tử gán)

//     =	    Gán	x = 5
//     +=	    Gán cộng	x += 3 // x = x + 3
//     -=	    Gán trừ	x -= 3 // x = x - 3
//     *=	    Gán nhân	x *= 3 // x = x * 3
//     /=	    Gán chia	x /= 3 // x = x / 3
//     %=	    Gán chia lấy dư	x %= 3 // x = x % 3
//     <<=	    Gán dịch bit trái	x <<= 1
//     >>=	    Gán dịch bit phải	x >>= 1
//     >>>=	    Gán dịch bit phải không dấu	x >>>= 1
//     &=	    Gán AND bit	x &= 3
//     `	    =`	Gán OR bit
//     ^=	    Gán XOR bit	x ^= 3