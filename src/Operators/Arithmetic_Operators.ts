// 1. Arithmetic Operators (Toán tử số học)

// +	Phép cộng	        5 + 3               // 8
// -	Phép trừ	        5 - 3               // 2
// *	Phép nhân	        5 * 3               // 15
// /	Phép chia	        5 / 3               // 1.6667
// %	Phép chia lấy dư	5 % 3               // 2
// ++	Tăng thêm 1	        let x = 5; x++      // 6
// --	Giảm đi 1	        let x = 5; x--      // 4