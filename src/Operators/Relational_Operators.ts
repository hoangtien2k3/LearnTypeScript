//2. Relational Operators (Toán tử quan hệ)

// ==	    Bằng nhau (so sánh lỏng lẻo)	        5 == "5" // true
// ===	    Bằng nhau (so sánh chặt chẽ)	        5 === "5" // false
// !=	    Không bằng nhau (lỏng lẻo)	            5 != "5" // false
// !==	    Không bằng nhau (chặt chẽ)	            5 !== "5" // true
// >	    Lớn hơn	                                5 > 3 // true
// <	    Nhỏ hơn	                                5 < 3 // false
// >=	    Lớn hơn hoặc bằng	                    5 >= 5 // true
// <=	    Nhỏ hơn hoặc bằng	                    5 <= 4 // false